    public void sendState(){
        int bright = brightness.getProgress();
        int r = red.getProgress();
        int g = green.getProgress();
        int b = blue.getProgress();
        sendCommand(String.format("light?bright=%s&r=%s&g=%s&b=%s",bright,r,g,b));
    }

    public void sendCommand(String command) {
        // Instantiate the RequestQueue.
        RequestQueue queue = Volley.newRequestQueue(getBaseContext());
        String url = "http://192.168.0.169:8080/" + command;

        // Request a string response from the provided URL.
        StringRequest stringRequest = new StringRequest(Request.Method.GET, url,
                new Response.Listener<String>() {
                    @Override
                    public void onResponse(String response) {
                        // Display the first 500 characters of the response string.
                        //mTextView.setText("Response is: "+ response.substring(0,500));
                        //Toast.makeText(MainActivity.this, response.substring(0, 500), Toast.LENGTH_SHORT).show();
                    }
                }, new Response.ErrorListener() {
            @Override
            public void onErrorResponse(VolleyError error) {
                //mTextView.setText("That didn't work!");
            }
        });
        // Add the request to the RequestQueue.
        queue.add(stringRequest);
    }

